    private void alterarTela(javax.swing.JPanel pnl) {
        pnlPrincipal.removeAll();
        pnlPrincipal.add(pnl);
        pnlPrincipal.repaint();
        pnlPrincipal.revalidate();
        
        switch (pnl.getName()) {
            case "login":
                logTxtUsuario.setText("");
                logPassSenha.setText("");
                break;
                
            case "cadastro":
                cadTxtNome.setText("");
                cadTxtCPF.setText("");
                cadTxtUsuario.setText("");
                cadPassSenha.setText("");
                break;
                
                /*
            case "cliente":
                break;
                */
                
            case "reservar":
                // Carregar servi√ßos do banco
                //
                resChoData.setMinDate(resChoData.getDefaultPeriods().getFirstDate());
                resBoxServico.setSelectedIndex(0);
                break;
                
            case "excluir":
                // Carregar reservas do banco
                //
                for (int i = 0; i < excTblReservas.getRowCount(); i++) {
                    excTblReservas.setValueAt(false, i, 4);
                }
                break;
                
            case "funcionario":
                
                funChoData.setSelectedDate(funChoData.getDefaultPeriods().getFirstDate());
                break;
        }
    }
    
